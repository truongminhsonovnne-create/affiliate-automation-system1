// =============================================================================
// Helper functions
// =============================================================================

type ColorCategory = keyof typeof CONVERSION_COLORS;
type ColorShade = keyof typeof CONVERSION_COLORS.primary;

/**
 * Get color value from color tokens
 */
export function getConversionColor(category: ColorCategory, shade: ColorShade): string {
  return CONVERSION_COLORS[category][shade];
}

type SpacingKey = keyof typeof CONVERSION_SPACING;

/**
 * Get spacing value
 */
export function getConversionSpacing(space: SpacingKey): string | number {
  return CONVERSION_SPACING[space];
}

type RadiusKey = keyof typeof CONVERSION_RADIUS;

/**
 * Get border radius value
 */
export function getConversionRadius(radius: RadiusKey): string {
  return CONVERSION_RADIUS[radius];
}
